public class Imprumut {
    private int id;
    private int idAbonament;
    private int idCarte;
    private String data;

    public int getId() {
        return id;
    }

    public int getIdAbonament() {
        return idAbonament;
    }

    public int getIdCarte() {
        return idCarte;
    }

    public String getData() {
        return data;
    }

    public void setId(int i) {
        this.id = i;
    }

    public void setIdAbonament(int i) {
        this.idAbonament = i;
    }

    public void setIdCarte(int i) {
        this.idCarte = i;
    }

    public void setData(String s) {
        this.data = s;
    }
}
