public class FisaCarte {
    private int idCarte;
    private int idLocatie;
    private boolean stare;

    public int getIdCarte() {
        return idCarte;
    }

    public int getIdLocatie() {
        return idLocatie;
    }

    public boolean getStare() {
        return stare;
    }

    public void setIdCarte(int i) {
        this.idCarte = i;
    }

    public void setIdLocatie(int i) {
        this.idLocatie = i;
    }

    public void setStare(boolean b) {
        this.stare = b;
    }
}
